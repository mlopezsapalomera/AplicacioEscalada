package objets;

public class Llarg {
    private int id;
    private int viaId;
    private int numeroLlarg;
    private int llargada;
    private String dificultat;
    private String orientacio;
    private String estat;
    private String dataNoAptaUntil;
    private String ancoratge;

    // Getters y Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getViaId() {
        return viaId;
    }

    public void setViaId(int viaId) {
        this.viaId = viaId;
    }

    public int getNumeroLlarg() {
        return numeroLlarg;
    }

    public void setNumeroLlarg(int numeroLlarg) {
        this.numeroLlarg = numeroLlarg;
    }

    public int getLlargada() {
        return llargada;
    }

    public void setLlargada(int llargada) {
        this.llargada = llargada;
    }

    public String getDificultat() {
        return dificultat;
    }

    public void setDificultat(String dificultat) {
        this.dificultat = dificultat;
    }

    public String getOrientacio() {
        return orientacio;
    }

    public void setOrientacio(String orientacio) {
        this.orientacio = orientacio;
    }

    public String getEstat() {
        return estat;
    }

    public void setEstat(String estat) {
        this.estat = estat;
    }

    public String getDataNoAptaUntil() {
        return dataNoAptaUntil;
    }

    public void setDataNoAptaUntil(String dataNoAptaUntil) {
        this.dataNoAptaUntil = dataNoAptaUntil;
    }

    public String getAncoratge() {
        return ancoratge;
    }

    public void setAncoratge(String ancoratge) {
        this.ancoratge = ancoratge;
    }
}
